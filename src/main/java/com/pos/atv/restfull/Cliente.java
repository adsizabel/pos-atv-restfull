package com.pos.atv.restfull;

/**
 *
 * @author Izabel Silva
 */


public class Cliente {
    private String nome;
    private String id;

    public Cliente() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Cliente{" + "nome=" + nome + ", id=" + id + '}';
    }
    
    
}
